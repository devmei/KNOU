v3 <- append(v2, 8, after=5)
v3
## [1] 3 4 5 6 7 8


## 예제 3-11 아래의 '규칙'에 따라 생성된 벡터 x를 sort() 함수를 이용하여 오름차순으로 정렬하고,
##           rank() 함수를 통해 자료의 순위를 출력해 보자
##           또한 order() 함수를 이용하여 오름차순에 의한 자료의 위칫값을 출력해 보자

# 규칙 : 3을 세 번 반복한 뒤 3에서 7까지 2만큼의 증가분으로 수열을 생성하여 덧붙인다
#        이어서 rev() 함수를 이용해 3에서 7까지 이르는 크기 3인 벡터의 역순을 구해 연결하고 4를 세 번 반복한 벡터를 덧붙인다

x <- c(rep(3, 3), seq(3, 7, by=2), rev(seq(3, 7, length=3)), rep(4, 3))
x
## [1] 3 3 3 3 5 7 7 5 3 4 4 4

sort(x)   # 벡터 x를 오름차순으로 정렬
## [1] 3 3 3 3 3 4 4 4 5 5 7 7

rank(x)   # 자료의 순위를 출력
## [1]  3.0  3.0  3.0  3.0  9.5 11.5 11.5  9.5  3.0  7.0  7.0  7.0

order(x)  # 오름차순에 의한 자료의 위칫값 출력
## [1]  1  2  3  4  9 10 11 12  5  8  6  7