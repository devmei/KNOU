## 예제 3-10 3에서 7까지의 값을 갖는 벡터 v2를 생성하고 replace() 함수를 이용하여 v2의 두 번째 원소를 10으로 바꾸어 출력해 보자
##           또한 append() 함수를 이용하여 v2의 다섯 번째 자료 다음에 8을 삽입하여 출력해 보자

