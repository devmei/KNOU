## 예제 3-6  seq() 함수를 이용하여 3에서 7까지의 자연수로 이루어진 벡터를 생성하여 보자

seq(from=3, to=7, by=1)  # from=시작 값, to=종료 값, by=증가분
## [1] 3 4 5 6 7

seq(to=7, from=3, by=1)  # to=종료 값, from=시작 값, by=증가분
## [1] 3 4 5 6 7

seq(by=1, from=3, to=7)  # by=증가분, from=시작 값, to=종료 값
## [1] 3 4 5 6 7

seq(3, 7, 1)             # 생략 시 from=시작 값, to=종료 값, by=증가분 순서로 인식
## [1] 3 4 5 6 7


## 예제 3-7  seq() 함수의 조건을 지정하여 벡터를 생성해 보자

seq(from=3, to=7, length=3)      # from=시작 값, to=종료 값, 조건지정
## [1] 3 5 7

seq(from=1, by=0.05, along=1:5)  # from=시작 값, to=종료 값, 조건지정
## [1] 1.00 1.05 1.10 1.15 1.20

seq(from=1, to=5, along=1:6)     # from=시작 값, to=종료 값, 조건지정
## [1] 1.0 1.8 2.6 3.4 4.2 5.0


## 예제 3-8  c() 함수를 이용하여 기본 벡터를 생성한 뒤 rep() 함수를 이용하여 생성된 벡터의 일정 원소가 반복되는 벡터를 만들어 보자

rep(c(1, 2), times=2)        # 1과 2로 구성된 벡터를 2회 반복
## [1] 1 2 1 2

rep(1:2, times=2)            # 1:2 1에서 2까지로 구성된 벡터를 2회 반복
## [1] 1 2 1 2

rep(c(2, 4), times=c(2, 1))  # 앞의 원소는 2회, 뒤의 원소는 1회 반복
## [1] 2 2 4

rep(c(2, 4), each=2)         # 앞, 뒤 모든 원소를 2회 반복
## [1] 2 2 4 4

rep(c(2, 4, 8), length=5)    # 2, 4, 8로 구성된 벡터를 반복하되 생성되는 벡터의 크기를 5로 한정
## [1] 2 4 8 2 4


## 예제 3-9  11에서 20까지의 값을 갖는 벡터 v1을 생성하고 세 번째와 다섯 번째 원소를 한꺼번에 출력해 보자
##           그리고 v1의 원소 중 15보다 큰 값만을 출력하자
##           v1 중 두 번째와 네 번째 값을 삭제하고 나머지 원소만을 출력해 보자

v1 <- c(11:20)  # 11에서 20까지의 값을 갖는 벡터 v1 생성
v1
